## 2. 작업 폴더에 풀기

받은 zip 을 본인 프로젝트 루트에 풀면 `.claude/` 폴더가 생성됩니다.

```
my-project/
├─ .claude/
│  ├─ commands/
│  │  ├─ run-ai-tasks.md
│  │  └─ review-uw.md
│  └─ ...
├─ src/
└─ ...
```

## 3. MCP 키 발급

GNB 우상단 아바타 → **MCP 키 관리** 에서 키를 발급해 둡니다.
이 키가 있어야 Claude Code 가 SPECODE 와 통신할 수 있습니다.

