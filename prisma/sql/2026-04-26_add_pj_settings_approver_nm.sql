--
-- 컬럼:
--   approver_nm
--     출력 docx 표지 / 변경이력 표의 "승인자" 기본값
--     자유 텍스트 — 멤버 select 가 아닌 이유는 외부 PM(고객사 측) 도 가능하게 하기 위함
